public class Car {
    private String name;
    private String whatfirm;
    private int  speed ;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getWhatfirm() {
        return whatfirm;
    }

    public void setWhatfirm(String whatfirm) {
        this.whatfirm = whatfirm;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }
}
